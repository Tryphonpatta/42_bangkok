char	*ft_strupcase(char *str)
{
	int	size;

	size = 0;
	while (*str)
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str -= 32;
		}
		str ++;
		size ++;
	}
	return (str - size);
}
