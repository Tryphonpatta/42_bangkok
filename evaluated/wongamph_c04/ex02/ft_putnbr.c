#include <unistd.h>

void	p(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	else if (nb < 0)
	{
		nb *= -1;
		p('-');
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		p(nb % 10 + 48);
	}
	else
	{
		p(nb + 48);
	}
}
