#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	a;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		nb *= -1;
		write(1, "-", 1);
	}
	if (nb < 10)
	{
		a = nb + '0';
		write(1, &a, 1);
		return ;
	}
	ft_putnbr(nb / 10);
	a = nb % 10 + '0';
	write(1, &a, 1);
}

