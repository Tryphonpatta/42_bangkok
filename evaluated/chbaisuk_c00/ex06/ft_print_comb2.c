#include <unistd.h>

void	ft_print_comb2(void)
{
	char	front[2];
	char	back[2];
	int		c[2];

	c[0] = 0;
	while (c[0] <= 98)
	{
		c[1] = c[0] + 1;
		while (c[1] <= 99)
		{
			front[1] = (c[0] % 10) + '0';
			front[0] = (c[0] / 10) + '0';
			back[1] = (c[1] % 10) + '0';
			back[0] = ((int)(c[1] / 10)) + '0';
			write(1, front, 2);
			write(1, " ", 1);
			write(1, back, 2);
			if (c[0] != 98 || c[1] != 99)
				write(1, ", ", 2);
		c[1]++;
		}
		c[0]++;
	}
}
