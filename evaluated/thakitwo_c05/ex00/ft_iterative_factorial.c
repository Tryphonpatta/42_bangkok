int	ft_iterative_factorial(int nb)
{
	int	ans;
	int	i;

	i = 1;
	ans = 1;
	if (nb < 0)
		return (0);
	while (i <= nb)
	{
		ans *= i;
		i++;
	}
	return (ans);
}
