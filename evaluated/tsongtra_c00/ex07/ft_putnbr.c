#include<unistd.h>

void	ft_putnbr(int nb)
{
	if (nb < 0 && nb <= -10)
	{
		write(1, "-", 1);
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		nb = nb % 10;
	}
	if (nb <= -10)
	{
		ft_putnbr(nb / -10);
		nb = nb % -10;
	}
	if (nb < 10)
	{
		if (nb < 0)
		{
			nb = -nb;
		}
		nb = nb + '0';
		write(1, &nb, 1);
	}
}
