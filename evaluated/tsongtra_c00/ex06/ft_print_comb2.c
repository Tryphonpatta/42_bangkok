#include<unistd.h>

void	ft_print_comb2(void)
{
	char	a[2];
	char	b[2];
	int		n[2];

	n[0] = 0;
	while (n[0] <= 98)
	{
		n[1] = n[0] + 1;
		while (n[1] <= 99)
		{
			a[0] = n[0] / 10 + '0';
			a[1] = (n[0] % 10) + '0';
			b[0] = n[1] / 10 + '0';
			b[1] = (n[1] % 10) + '0';
			write(1, a, 2);
			write(1, " ", 1);
			write(1, b, 2);
			if (n[0] == 98 && n[1] == 99)
				break ;
			write(1, ", ", 2);
			n[1]++;
		}
		n[0]++;
	}
}
