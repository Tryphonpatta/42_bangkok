#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	nbr;

	nbr = nb + 48;
	if (nb < 0)
	{
		write(1, "-", 1);
		if (nb == -2147483648)
		{
			ft_putnbr(214748364);
			write(1, "8", 1);
			return ;
		}
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		write(1, &nbr, 1);
	}
}
