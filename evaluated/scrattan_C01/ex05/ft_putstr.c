#include <unistd.h>

void	ft_putchr(char ch)
{
	write(1, &ch, 1);
}

void	ft_putstr(char *str)
{
	int	n;

	n = 0;
	while (str[n] != '\0')
	{
		ft_putchr(str[n]);
		n++;
	}
}
