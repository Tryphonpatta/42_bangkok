int	ft_sqrt(int nb)
{
	int	d;

	d = 1;
	if (nb == 0)
		return (0);
	while (d * d <= nb && d <= 46340)
	{
		if (d * d == nb)
			return (d);
		d ++;
	}
	return (0);
}
