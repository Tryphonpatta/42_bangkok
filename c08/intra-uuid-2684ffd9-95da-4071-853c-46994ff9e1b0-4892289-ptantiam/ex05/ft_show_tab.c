#include <unistd.h>
#include "ft_stock_str.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int c)
{
	if (c >= 10)
	{
		ft_putnbr(c / 10);
	}
	ft_putchar(c % 10 + 48);
}

void	ft_putstr(char *c)
{
	while (*c)
	{
		write(1, c, 1);
		c ++;
	}
}

void	ft_show_tab(struct s_stock_str *par)
{
	int	i;

	i = 0;
	while (par[i].str != 0)
	{
		ft_putstr(par[i].str);
		write(1, "\n", 1);
		ft_putnbr(par[i].size);
		write(1, "\n", 1);
		ft_putstr(par[i].copy);
		write(1, "\n", 1);
		i ++;
	}
}
